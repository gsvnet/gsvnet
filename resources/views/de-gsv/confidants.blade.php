            <h2>De vertrouwenspersonen</h2>
            <p>
                Binnen de GSV zijn er twee vertrouwenspersonen. Deze vertrouwenspersonen zijn bedoeld als aanspreekpunt voor jou als lid wanneer je behoefte hebt aan een vertrouwelijk gesprek. Je kunt met hen problemen bespreken in de breedste zin van het woord. Of het nu gaat om problemen binnen de GSV, persoonlijke problemen of misschien wel om zorgen over anderen. Zij staan open voor een gesprek en zullen vertrouwelijk met de besproken inhoud omgaan. Het kan zijn dat het na een gesprek wenselijk is om stappen te ondernemen. De vertrouwenspersonen helpen jou om die stappen te zetten of zullen na overleg zelf in actie komen.
            </p>
            <p>
                Hoewel zij vanuit de vereniging opereren en door de Senaat zijn aangesteld, werken zij volledig onafhankelijk en delen geen informatie met derden. Wanneer de vertrouwenspersoon inschat dat het meerwaarde heeft om hetgeen dat besproken is te delen met derden, gebeurt dat slechts met jouw expliciete toestemming. Uitzonderingen hierop zijn te vinden bij <a href="#zwijgplicht">‘doorbreken zwijgplicht’</a>.
            </p>

            <h4>Cursussen</h4>
            <p>
                Vanuit de gemeente is er beleid gekomen waarin staat dat de vertrouwenspersonen aanvullende training gaan krijgen. Mogelijk wordt deze training samengevoegd met een suïcidepreventietraining. Door het volgen van cursussen zullen de vertrouwenspersonen nog beter gekwalificeerd zijn voor het uitvoeren van hun functie.
            </p>

            <h4 id="zwijgplicht">Doorbreken zwijgplicht</h4>
            <p>
                De vertrouwenspersoon kan de geheimhouding doorbreken als jij daartoe expliciet toestemming geeft, maar ook wanneer die toestemming ontbreekt en er zeer dringende en ernstige redenen aanwezig zijn waardoor de vertrouwenspersoon zich kan beroepen op een ‘conflict van plichten’.
                In dat geval moet echter tenminste voldaan zijn aan al de hieronder genoemde zeven voorwaarden, opgesteld door de Landelijke Vereniging Vertrouwenspersonen:
            </p>
            <ol>
                <li>Alles is in het werk gesteld de toestemming van de betrokken persoon te verkrijgen;</li>
                <li>De vertrouwenspersoon verkeert in gewetensnood door het handhaven van de geheimhouding;</li>
                <li>Er is geen andere weg dan doorbreking van de geheimhouding om het probleem op te lossen;</li>
                <li>Het is vrijwel zeker dat het niet-doorbreken van de geheimhouding voor betrokkenen of voor derden aanwijsbare en ernstige schade en/of gevaar zal opleveren;</li>
                <li>De vertrouwenspersoon is er vrijwel zeker van dat de doorbreking van de geheimhouding die schade aan de betrokkenen of anderen in belangrijke mate zal voorkomen of beperken;</li>
                <li>Indien een dergelijke situatie zich voordoet, dient de vertrouwenspersoon de redenen om de geheimhouding te doorbreken met een ter zake kundige partij te bespreken alvorens de geheimhouding te doorbreken;</li>
                <li>De vertrouwenspersoon brengt betrokkene op de hoogte van het voornemen de geheimhouding te doorbreken alvorens dit daadwerkelijk te doen;</li>
                <li>De vertrouwenspersoon verwijst betrokkene waar nodig naar een andere vertrouwenspersoon binnen of buiten de organisatie.</li>
            </ol>


        <h2>De jaren vanaf 1966</h2>
        <p>Dit ging twaalf leden van de vereniging te ver, en deze besloten dan ook op 23 juni 1966 de ‘Gereformeerde Studenten Vereniging’ op te richten. In datzelfde jaar stelde Hendrik de Cock zich open voor niet-Vrijgemaakten. Toch gingen er over de jaren binnen de GSV ook steeds meer stemmen op om de vereniging open te stellen. Eerst was er sprake van ‘gastleden’ maar al gauw wilde men ook daar van af. In 2004 is uiteindelijk besloten om de GSV open te stellen, maar met de voorwaarde dat alle leden een intentieverklaring zouden ondertekenen waarin ze de Gereformeerde grondslag van de vereniging zouden onderschrijven. GSV was niet zozeer meer een ‘verlengstuk van de kerk’, maar koos er nu voor om een zelfstandige organisatie te zijn met een duidelijke ‘gereformeerde’ identiteit. Iedereen die denkt te passen bij die verenigingsidentiteit kon dus volwaardig lid worden. De Vanaf 2004 ging de GSV zich ook steeds meer focussen op vier ‘pijlers’: christelijk, sociaal, intellectueel en studentikoos. Dit waren volgens de leden de aspecten van de vereniging die waar ze zich op moest gaan richten.</p>

        <h2>En nu?</h2>
        <p>De GSV is tegenwoordig een moderne vereniging die haar eigen dansfeesten, competities, kampen en soosavonden organiseert. In vergelijking met 1966 is de vereniging erg veranderd, maar dat betekent ook dat er meer ruimte is gekomen voor diversiteit en andere meningen. Ook heeft de GSV sinds de laatste decennia meer gezelschap gekregen van verenigingen met een meer evangelische inslag die ook haar stempel drukken op het studentenleven en waarmee ook is besloten om een Overleg Christelijk Studentenverenigingen Groningen op te richten die ook wil proberen over verschillen heen te kijken en gezamenlijk een christelijk geluid in Groningen te laten horen. De GSV blijft echter een vereniging vanuit een stevig gereformeerd fundament wat zich uit in een ruimte voor kritiek, een focus op intellectualiteit maar ook op omzien naar elkaar. Vanuit die basis is zij een plek te zijn waar sociale, intellectuele, studentikoze, maar bovenal ook christelijke activiteiten centraal staan, voor haar leden van nu en van de toekomst.</p>




